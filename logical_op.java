public class logical_op{
    public static void main(String[] args){
        // logical operators
        // &&, ||, !

        System.out.println((24>2) && ((24<40)));
        System.out.println((24<2) && ((24<40)));
        System.out.println((24==2) || ((24<40)));
        System.out.println((24!=2) || ((24>40)));

    }
}