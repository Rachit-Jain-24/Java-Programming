public class relational_op{
    public static void main(String[] args){
        // relational operators:
        // ==, !=, >, <, >=, <=.

        int a = 23;
        int b = 24;

        System.out.println((a==b));
        System.out.println((a!=b));
        System.out.println((a<b));
        System.out.println((a>b));
        System.out.println((a>=b));
        System.out.println((a<=b));

    }
}