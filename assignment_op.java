public class assignment_op{
    public static void main(String[] args){
        //assignment operators
        // =,+=,-=,*=,/=

        int a = 100;
        int b = 20;

        System.out.println(a=b);
        System.out.println(a+=2);
        System.out.println(b-=10);
        System.out.println(b*=2);
        System.out.println(a/=5);
    }
}